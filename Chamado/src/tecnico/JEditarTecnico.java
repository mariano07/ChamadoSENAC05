    private void button_salvarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_button_salvarMouseClicked
        boolean tf = false;
        String usuario=text_usuario.getText(),senha=pfield_senha.getText();
        if(!text_usuario.getText().equals("Usuário") && !pfield_senha.getText().equals("") && !pfield_confsenha.getText().equals("") && pfield_senha.getText().equals(pfield_confsenha.getText()) ){
            tf = cbd.EditaUsuario(matricula,usuario,senha);
            if(tf == false){
                JOptionPane.showMessageDialog(null, "Usuário e Senha alterado com sucesso!\nPor favor faça o login novamente.");
                dispose();
            }else{
                JOptionPane.showMessageDialog(null, "Usuário não presente no banco de dados!");
            }
        }else if(!pfield_senha.getText().equals(pfield_confsenha.getText())){
            JOptionPane.showMessageDialog(null, "Senhas não coincidem!");
        }else{
            JOptionPane.showMessageDialog(null, "Preencha todos os campos corretamente.");
        }
    }//GEN-LAST:event_button_salvarMouseClicked

